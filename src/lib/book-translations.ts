/**
 * Get the localized title + slug for a book.
 * Falls back to the English title/slug if no translation exists.
 */
export function localizeBook(
  enSlug: string,
  enTitle: string,
  locale: string,
): { title: string; slug: string; image?: string } {
  const map = LIBRARY_BOOK_TRANSLATIONS[locale];
  const entry = map?.[enSlug];
  return {
    title: entry?.title ?? enTitle,
    slug: entry?.slug ?? enSlug,
    image: entry?.image,
  };
}
